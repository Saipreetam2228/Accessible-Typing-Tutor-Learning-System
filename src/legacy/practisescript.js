// -- Update hands -------------------------------------------
function applyPos(el, pos) {
  if (!pos) return;
  function normalizeHandBottom(value) {
    if (
      typeof value !== "string" ||
      value.charAt(0) !== "-" ||
      value.slice(-2) !== "vh"
    )
      return value;
    var amount = parseFloat(value);
    if (amount <= -49) return "clamp(-300px, -33vh, -210px)";
    if (amount <= -48) return "clamp(-295px, -32vh, -205px)";
    if (amount <= -46) return "clamp(-280px, -31vh, -200px)";
    if (amount <= -45) return "clamp(-270px, -30vh, -190px)";
    return "clamp(-250px, -28vh, -180px)";
  }
  el.style.left = pos.left !== undefined ? pos.left : "";
  el.style.right = pos.right !== undefined ? pos.right : "";
  el.style.bottom =
    pos.bottom !== undefined ? normalizeHandBottom(pos.bottom) : "";
  el.style.transform = pos.transform !== undefined ? pos.transform : "";
}

