// Pre-render spans for current stage (called once per stage)
function renderStageSpans() {
  var keysEl = document.getElementById("keysToTypeSpan");
  var correctText = stages[currentStage] || "";

  var activeLine = document.createElement("div");
  activeLine.className = "paraLine active";

  for (var i = 0; i < correctText.length; i++) {
    var span = document.createElement("span");
    span.className = "pending";
    span.textContent = correctText[i] === " " ? "\u00A0" : correctText[i];
    activeLine.appendChild(span);
  }

  var lastVisible = Math.min(currentStage + 4, stages.length);
  var upcomingHtml = "";
  for (var s = currentStage + 1; s < lastVisible; s++) {
    var safe = stages[s]
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;");
    upcomingHtml += "<div class='paraLine'>" + safe + "</div>";
  }

  keysEl.innerHTML = "";
  keysEl.appendChild(activeLine);
  keysEl.insertAdjacentHTML("beforeend", upcomingHtml);
}

// Display update - swaps classes only, never rebuilds DOM
function updateDisplay() {
  var msgEl = document.getElementById("message");
  var lnum = document.querySelector(".lh");
  var rnum = document.querySelector(".rh");
  var correctText = stages[currentStage] || "";
  var activeLine = document.querySelector(".paraLine.active");

  if (!activeLine || activeLine.children.length === 0) {
    renderStageSpans();
    activeLine = document.querySelector(".paraLine.active");
  }

  var spans = activeLine.querySelectorAll("span");
  for (var i = 0; i < spans.length; i++) {
    if (i < userInput.length) {
      spans[i].className =
        userInput[i] === correctText[i] ? "correct" : "wrong";
    } else if (i === userInput.length) {
      spans[i].className = "cursor";
    } else {
      spans[i].className = "pending";
    }
  }

  msgEl.textContent = "Line " + (currentStage + 1) + " of " + stages.length;

  if (userInput.length < correctText.length) {
    var nextChar = correctText[userInput.length];
    applyNextKeyHighlight(nextChar);
    updateHands(nextChar);
  } else {
    clearNextKeyHighlight();
    if (lnum) lnum.src = "assets/images/letters/left_idle.webp";
    if (rnum) rnum.src = "assets/images/letters/right_idle.webp";
  }
}

