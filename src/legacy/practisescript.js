// -- Timer --------------------------------------------------
function startTimer() {
  startTime = Date.now();
  timerInterval = setInterval(function () {}, 1000);
}

function stopTimer() {
  clearInterval(timerInterval);
  timerInterval = null;
  return startTime ? Math.floor((Date.now() - startTime) / 1000) : 0;
}

function formatTime(secs) {
  var m = Math.floor(secs / 60)
    .toString()
    .padStart(2, "0");
  var s = (secs % 60).toString().padStart(2, "0");
  return m + ":" + s;
}

function generateId() {
  return Date.now().toString(36) + Math.random().toString(36).slice(2, 7);
}

function saveSession() {
  var totalChars = stages.reduce(function (a, s) {
    return a + s.length;
  }, 0);
  var totalWords = stages.reduce(function (a, s) {
    return a + s.trim().split(/\s+/).length;
  }, 0);
  var elapsed = startTime ? Math.floor((Date.now() - startTime) / 1000) : 0;
  var correct = sessionKeystrokes.filter(function (k) {
    return k.correct;
  }).length;
  var total = sessionKeystrokes.length;
  var accuracy = total > 0 ? Math.round((correct / total) * 100) : 100;
  var wpm = elapsed > 0 ? Math.round(totalWords / (elapsed / 60)) : 0;
  var profileId = localStorage.getItem("vv_activeProfile") || "guest";

  var session = {
    session_id: generateId(),
    profileId: profileId,
    timestamp: new Date().toISOString(),
    mode: "practise",
    topic: currentTopicKey,
    total_time_seconds: elapsed,
    total_words: totalWords,
    total_characters: totalChars,
    wpm: wpm,
    accuracy_percent: accuracy,
    keystrokes: sessionKeystrokes,
  };

  var history = [];
  try {
    history = JSON.parse(localStorage.getItem("typingHistory")) || [];
  } catch (e) {}
  history.push(session);
  localStorage.setItem("typingHistory", JSON.stringify(history));
}

