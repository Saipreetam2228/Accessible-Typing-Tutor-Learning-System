function setVisualTheme(mode, animate) {
  var body = document.body;
  var isDark = mode === "dark";
  var transitionClass = isDark ? "transitioning-dark" : "transitioning-light";
  var lightBtn = document.getElementById("themeLightBtn");
  var darkBtn = document.getElementById("themeDarkBtn");

  if (animate) body.classList.add(transitionClass);

  window.setTimeout(
    function () {
      body.classList.toggle("dark-mode", isDark);
      if (lightBtn) lightBtn.classList.toggle("active", !isDark);
      if (darkBtn) darkBtn.classList.toggle("active", isDark);
      document.getElementById("theme").href =
        "css/" + (isDark ? "black" : "color") + ".css";
      themeMode = isDark ? "black" : "color";
      localStorage.setItem("type-tutor-theme-preference", mode);

      if (animate) {
        window.setTimeout(function () {
          body.classList.remove(transitionClass);
        }, 2000);
      }
    },
    animate ? 40 : 0,
  );
}

