function applyNextKeyHighlight(char) {
  clearNextKeyHighlight();
  if (!highlightEnabled) return;
  var leftKeys = [
    "q",
    "w",
    "e",
    "r",
    "t",
    "a",
    "s",
    "d",
    "f",
    "g",
    "z",
    "x",
    "c",
    "v",
    "b",
  ];
  var rightKeys = ["y", "u", "i", "o", "p", "h", "j", "k", "l", "n", "m"];
  var lShiftSym = ["!", "@", "#", "$", "%"];
  var rShiftSym = ["^", "&", "*", "(", ")", "+", "_", "~", ":", '"', "?", ">"];
  var isUpper = char.length === 1 && /[A-Z]/.test(char);
  var lc = char.toLowerCase();
  var target;

  if (char === " ") target = document.querySelector('[data-key="32"]');
  else if (char === "'" || char === '"')
    target = document.querySelector('[data-key="222"]');
  else
    target = document.querySelector(
      '[data-char*="' + char.toUpperCase() + '"]',
    );

  if (target) {
    target.classList.add("next-key-highlight");
    highlightEl = target;
  }

  if (isUpper && leftKeys.includes(lc))
    document
      .querySelector('[data-key="16-R"]')
      ?.classList.add("next-key-highlight");
  else if (isUpper && rightKeys.includes(lc))
    document
      .querySelector('[data-key="16"]')
      ?.classList.add("next-key-highlight");
  else if (lShiftSym.includes(char))
    document
      .querySelector('[data-key="16-R"]')
      ?.classList.add("next-key-highlight");
  else if (rShiftSym.includes(char))
    document
      .querySelector('[data-key="16"]')
      ?.classList.add("next-key-highlight");
}

